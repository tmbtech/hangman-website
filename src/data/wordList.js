/**
 * Get a random word from all available words
 */
export function getRandomWord() {
  return ALL_WORDS[Math.floor(Math.random() * ALL_WORDS.length)]
}

/**
 * Get a random word by difficulty level
 * @param {string} difficulty - 'easy', 'medium', or 'hard'
 */
export function getRandomWordByDifficulty(difficulty) {
  switch (difficulty.toLowerCase()) {
    case 'easy':
      return EASY_WORDS[Math.floor(Math.random() * EASY_WORDS.length)]
    case 'medium':
      return MEDIUM_WORDS[Math.floor(Math.random() * MEDIUM_WORDS.length)]
    case 'hard':
      return HARD_WORDS[Math.floor(Math.random() * HARD_WORDS.length)]
    default:
      return getRandomWord()
  }
}

